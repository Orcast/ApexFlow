    transaction_time DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '交易时间',
    remark VARCHAR(200) COMMENT '备注',
    FOREIGN KEY (order_id) REFERENCES apexflow_order(id)
) COMMENT='财务收支记录表';

